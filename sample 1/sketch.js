function setup() {
  createCanvas(600, 400);
  noLoop();
}

function draw() {
  background(200, 220, 255); // 하늘색 배경

  // 얼굴 (원)
  fill(255, 220, 180);
  ellipse(300, 200, 200, 200);

  // 왼쪽 눈
  fill(0);
  ellipse(260, 180, 20, 20);

  // 오른쪽 눈
  ellipse(340, 180, 20, 20);

  // 코 (삼각형)
  fill(255, 180, 150);
  triangle(300, 190, 290, 220, 310, 220);

  // 입 (사각형)
  fill(255, 100, 100);
  rect(270, 240, 60, 20);

  // 머리 (큰 사각형 느낌)
  fill(100, 50, 0);
  rect(200, 100, 200, 80);

  // 장식용 원
  fill(255, 0, 0);
  ellipse(100, 100, 50, 50);

  fill(0, 255, 0);
  ellipse(500, 80, 40, 40);

  fill(0, 0, 255);
  ellipse(450, 300, 60, 60);
}
